    public void saveFile(String filepath, String code){
        try{
            BufferedWriter out = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(sFileName), sCode));
            // 方法一 : 儲存後檔案會分行，但讀取後分行會失效
            //out.write(jTextArea1.getText().toString());
            //方法二
             String arr[] = jTextArea1.getText().toString().split("\n");
            for (int i = 0; i < arr.length ;i++ )
            {
                out.write(arr[i]);
                out.newLine();//強制分行;
            }
            out.flush(); // 強制將資料寫入檔案，執行這行，資料才會寫入檔案
            out.close();
        }
        catch(Exception e)
        {
            
        }
        
    }
