        int result = JOptionPane.showConfirmDialog(rootPane, "按Yes用"+ sCode+"儲存，按No用Unicode儲存，按Cancel結束","請選擇編碼方式", JOptionPane.YES_NO_CANCEL_OPTION);
        this.setTitle(sFileName);
